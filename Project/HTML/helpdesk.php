<b>When will my shipment arrive?</b><br><br>

The BOXIT Tracking page can provide up-to-date information on your shipment location.<br><br>

<b>If a BOXIT Time Definite Delivery was not delivered by the specified time, can I request a credit from BOXIT?</b><br><br>

In the unlikely event that a Time Definite Delivery misses its scheduled delivery through our fault, you should contact your local customer service department who will arrange a credit for you.<br><br>

<b>How do I make a loss/damage claim for a BOXIT shipment that failed to arrive, or was damaged?</b><br><br>

If your shipment failed to arrive, it is important that you contact customer service with an accurate description of the goods and their packaging as soon as possible. Our “found shipment centers” hold on to items that were not properly addressed or that became detached from their packaging. It is possible that they will be able to reunite you with your shipment. If this fails, please contact customer servicecontact customer service to file a report.<br><br>
Please note that damage to goods in transit is frequently the result of inadequate packaging.<br><br>

<b>What is the maximum size of package I can send?</b><br><br>
Due to the size of the door openings on our vans and planes, we ask that a shipment does not exceed 120 cm in dimension. We will be pleased to advise and arrange carriage of any larger items for you. In addition, for the safety of our couriers, we require notification of shipments over 50kg in weight.<br><br>

<b>I intend to buy some products from an overseas vendor that uses BOXIT to ship. Will I have to pay import duty on the items I purchase?</b><br><br>

You will have to discuss this with the vendor. It depends on the type of commodity you are purchasing, the value of the goods, how the vendor labels them for customs purposes and what country you are importing them into. If customs imposes a charge BOXIT will pay this in the first instance, however you are advised to pay this, whether or not you accept delivery of the goods.<br><br>

<b>Our company would like to begin exporting products to several countries. We have no previous experience of this, can BOXIT advise us?</b><br><br>

Absolutely! BOXIT has unrivalled experience of international shipping. Please contact BOXIT Sales to discuss your needs.<br><br>

<b>I was charged duties and taxes by Customs for something purchased online. Why wasn’t that included as part of the overall shipping costs?</b><br><br>

Please be aware that when purchasing goods via internet sites, some or all of these goods may not originate in your home country. When goods are not sent domestically or within a single customs union, you are liable to pay any inbound duties and taxes which your local Customs authority deems appropriate. Duties and taxes are most typically not included in the price of the goods you purchase online. <br>

BOXIT pays the Customs authority on your behalf any duties and taxes that are due on the goods, when they enter the country. This simple process ensures that our courier can deliver your goods in shortest possible transit time. Please note that we only release the goods to you upon the full repayment of any duties and taxes that were paid on your behalf.<br>
